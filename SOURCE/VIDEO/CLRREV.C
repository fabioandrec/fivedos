#include <ClipApi.h>

//---------------------------------------------------------------------------//

BYTE ClrReverse( BYTE bAtt )
{
    return ( bAtt >> 4 ) | ( bAtt << 4 );
}

//---------------------------------------------------------------------------//

CLIPPER CLRREVERSE( void )
{
    _retni( ClrReverse( _parni( 1 ) ) );
}

